// Arithmetic Operations in the Go Language (Mathematics in Golang + - / *%)

package main

import (
	"fmt"
)

func main() {

	// +-*/
	// %

	var a float64 = 22
	var b float64 = 8

	var c int = 8
	var d int = 3

	fmt.Println("a + b =", a+b)

	fmt.Println("a - b =", a-b)

	fmt.Println("a * b =", a*b)

	fmt.Println("a / b =", a/b)

	fmt.Println("c % d =", c%d)

}
